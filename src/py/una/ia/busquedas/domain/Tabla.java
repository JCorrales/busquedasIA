
package py.una.ia.busquedas.domain;

/**
 *
 * @author juan corrales
 */
public class Tabla {
    private String criterio;
    private String costoUniforme;
    private String avaro;
    private String aEstrella;

    public String getCriterio() {
        return criterio;
    }

    public void setCriterio(String criterio) {
        this.criterio = criterio;
    }

    public String getCostoUniforme() {
        return costoUniforme;
    }

    public void setCostoUniforme(String costoUniforme) {
        this.costoUniforme = costoUniforme;
    }

    public String getAvaro() {
        return avaro;
    }

    public void setAvaro(String avaro) {
        this.avaro = avaro;
    }

    public String getaEstrella() {
        return aEstrella;
    }

    public void setaEstrella(String aEstrella) {
        this.aEstrella = aEstrella;
    }
}
